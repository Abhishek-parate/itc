<script>
// ✅ Find chatbot button by scanning ALL fixed/absolute elements in bottom-right area
function getChatbotButton() {
    // First try known selectors
    const knownSelectors = [
        '#chatbot-toggle', '.chatbot-toggle', '.chatbot-btn',
        '.chatbot-launcher', '.chat-toggle', '[id*="chatbot"]',
        '[class*="chatbot"]', '[class*="chat-btn"]', '[class*="chat-launcher"]'
    ];
    for (let sel of knownSelectors) {
        const el = document.querySelector(sel);
        if (el) return el;
    }

    // ✅ Fallback: find any button/div that is fixed and sitting in bottom-right corner
    const allElements = document.querySelectorAll('button, div, a');
    for (let el of allElements) {
        if (el.id === 'backToTop') continue; // skip our own button
        const style = window.getComputedStyle(el);
        const rect = el.getBoundingClientRect();
        if (
            style.position === 'fixed' &&
            rect.bottom > window.innerHeight * 0.6 && // in lower 40% of screen
            rect.right > window.innerWidth * 0.7 // in right 30% of screen
        ) {
            return el;
        }
    }
    return null;
}

function positionBackToTop() {
    const btn = document.getElementById('backToTop');
    const chatbotBtn = getChatbotButton();

    if (chatbotBtn) {
        const rect = chatbotBtn.getBoundingClientRect();

        // Gap between buttons: 16px on mobile, 24px on desktop
        const gap = window.innerWidth >= 768 ? 24 : 16;

        // Bottom = distance from bottom of screen to top of chatbot btn + gap
        const bottomValue = (window.innerHeight - rect.top) + gap;

        // Right = align to chatbot button's right edge
        const rightValue = window.innerWidth - rect.right;

        btn.style.bottom = bottomValue + 'px';
        btn.style.right = rightValue + 'px';
    } else {
        // Safe fallback if chatbot not found
        btn.style.bottom = window.innerWidth >= 768 ? '130px' : '100px';
        btn.style.right = '24px';
    }
}

// ✅ Scroll visibility toggle
window.addEventListener('scroll', function() {
    const btn = document.getElementById('backToTop');
    positionBackToTop();
    if (window.pageYOffset > 300) {
        btn.classList.remove('opacity-0', 'invisible');
        btn.classList.add('opacity-100', 'visible');
    } else {
        btn.classList.remove('opacity-100', 'visible');
        btn.classList.add('opacity-0', 'invisible');
    }
});

window.addEventListener('load', positionBackToTop);
window.addEventListener('resize', positionBackToTop);

function scrollToTop() {
    window.scrollTo({
        top: 0,
        behavior: 'smooth'
    });
}
</script>